#include <string.h>

int t[100001];
void ncr(int n, int* result) {
    int i, j;
    for (i = 0; i <= n; i++) {
        result[0] = result[i] = 1;
        for (j = 1; j < i; j++) {
            result[j] = (t[j - 1] + t[j]) % 1000000007;
        }
        memcpy(t, result, sizeof t);
    }
}

